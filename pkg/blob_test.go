package git

import (
	"bytes"
	"crypto/md5"
	"crypto/sha1"
	"crypto/sha256"
	"crypto/sha512"
	"testing"
)

var blobBytes = []byte{
	//0x62, 0x6c, 0x6f, 0x62, 0x20, 0x31, 0x33, 0x00,
	0x48, 0x65, 0x6c, 0x6c, 0x6f, 0x20, 0x57, 0x6f,
	0x72, 0x6c, 0x64, 0x21, 0x0a,
}

func TestBlob(t *testing.T) {
	var blob Blob
	if err := blob.UnmarshalBinary(blobBytes); err != nil {
		t.Fatalf("(*git.Blob).UnmarshalBinary failed: %v", err)
	}

	if expected := 13; blob.Size != int64(expected) {
		t.Fatalf("expected size %d, got %d", expected, blob.Size)
	}

	if expected := []byte("Hello World!\n"); !bytes.Equal(blob.Contents, expected) {
		t.Errorf("expected contents %q, got %q", expected, blob.Contents)
	}

	/*if expected := [sha1.Size]byte{
		0x98, 0x0a, 0x0d, 0x5f, 0x19, 0xa6, 0x4b, 0x4b,
		0x30, 0xa8, 0x7d, 0x42, 0x06, 0xaa, 0xde, 0x58,
		0x72, 0x6b, 0x60, 0xe3,
	}; blob.OID != expected {
		t.Errorf("expected OID %x, got %x", expected, blob.OID)
	}*/

	if expected := [md5.Size]byte{
		0x8d, 0xdd, 0x8b, 0xe4, 0xb1, 0x79, 0xa5, 0x29,
		0xaf, 0xa5, 0xf2, 0xff, 0xae, 0x4b, 0x98, 0x58,
	}; blob.MD5 != expected {
		t.Errorf("expected MD5 %x, got %x", expected, blob.MD5)
	}

	if expected := [sha1.Size]byte{
		0xa0, 0xb6, 0x59, 0x39, 0x67, 0x0b, 0xc2, 0xc0,
		0x10, 0xf4, 0xd5, 0xd6, 0xa0, 0xb3, 0xe4, 0xe4,
		0x59, 0x0f, 0xb9, 0x2b,
	}; blob.SHA1 != expected {
		t.Errorf("expected SHA1 %x, got %x", expected, blob.SHA1)
	}

	if expected := [sha256.Size]byte{
		0x03, 0xba, 0x20, 0x4e, 0x50, 0xd1, 0x26, 0xe4,
		0x67, 0x4c, 0x00, 0x5e, 0x04, 0xd8, 0x2e, 0x84,
		0xc2, 0x13, 0x66, 0x78, 0x0a, 0xf1, 0xf4, 0x3b,
		0xd5, 0x4a, 0x37, 0x81, 0x6b, 0x6a, 0xb3, 0x40,
	}; blob.SHA256 != expected {
		t.Errorf("expected SHA256 %x, got %x", expected, blob.SHA256)
	}

	if expected := [sha512.Size]byte{
		0x83, 0x04, 0x45, 0xe8, 0x6a, 0x0c, 0xfa, 0xfa,
		0xc4, 0xe1, 0x53, 0x10, 0x02, 0x35, 0x6f, 0x38,
		0x48, 0x47, 0xa1, 0x1a, 0x74, 0x56, 0xfb, 0x8c,
		0xcb, 0x81, 0xab, 0x36, 0xe3, 0x7b, 0xff, 0x28,
		0xf3, 0x4f, 0xa2, 0xc5, 0xbf, 0xdd, 0x34, 0x7e,
		0x96, 0x4c, 0x5c, 0x5d, 0xf0, 0xfc, 0x30, 0x5d,
		0xe6, 0x39, 0x43, 0x68, 0x21, 0x93, 0x07, 0xb2,
		0xce, 0xeb, 0x0e, 0xc8, 0x4b, 0x7c, 0x2b, 0x31,
	}; blob.SHA512 != expected {
		t.Errorf("expected SHA512 %x, got %x", expected, blob.SHA512)
	}

}
