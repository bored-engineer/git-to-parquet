package git

import "testing"

var treeBytes = []byte{
	//0x74, 0x72, 0x65, 0x65, 0x20, 0x36, 0x31, 0x00,
	0x34, 0x30, 0x30, 0x30, 0x30, 0x20, 0x62, 0x61,
	0x72, 0x00, 0x5c, 0xa9, 0xc1, 0x9d, 0x9a, 0x33,
	0x3a, 0x50, 0x43, 0x89, 0x4d, 0xa1, 0x64, 0xac,
	0xb6, 0x54, 0x57, 0x07, 0x32, 0xd7, 0x31, 0x30,
	0x30, 0x36, 0x34, 0x34, 0x20, 0x66, 0x6f, 0x6f,
	0x00, 0xe9, 0x65, 0x04, 0x7a, 0xd7, 0xc5, 0x78,
	0x65, 0x82, 0x3c, 0x7d, 0x99, 0x2b, 0x1d, 0x04,
	0x6e, 0xa6, 0x6e, 0xdf, 0x78,
}

func TestTree(t *testing.T) {
	var tree Tree
	if err := tree.UnmarshalBinary(treeBytes); err != nil {
		t.Fatalf("(*git.Tree).UnmarshalBinary failed: %v", err)
	}

	if len(tree.Entries) != 2 {
		t.Fatalf("expected 2 entries, got %d", len(tree.Entries))
	}

	if tree.Entries[0].Name != "bar" || tree.Entries[0].Mode != 0o40000 {
		t.Errorf("first entry mismatch: want 'bar', 0x4000, got %q 0o%x", tree.Entries[0].Name, tree.Entries[0].Mode)
	}

	if tree.Entries[1].Name != "foo" || tree.Entries[1].Mode != 0o100644 {
		t.Errorf("second entry mismatch: want 'foo', 0o100644, got %q 0o%x", tree.Entries[0].Name, tree.Entries[0].Mode)
	}
}
